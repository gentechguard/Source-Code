export const getDealerStats = (dealerList: Dealer[] = dealers) => {
  const activeDealers = dealerList.filter(d => d.is_active);
  return {
    total: activeDealers.length,
    premium: activeDealers.filter(d => d.dealer_type === 'premium').length,
    standard: activeDealers.filter(d => d.dealer_type === 'standard').length,
    comingSoon: activeDealers.filter(d => d.dealer_type === 'coming_soon').length,
    cities: new Set(activeDealers.map(d => d.city)).size,
    states: new Set(activeDealers.map(d => d.state)).size
  };
};
